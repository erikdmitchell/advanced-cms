<p>
	Below is a detailed description of each field (aka settings) for each custom post type:
</p>

<ul class="custom-post-type-list">
	<li>Name (string) - The post type name. It will be used in the admin menus as well.</li>
	<li>Label (string) - A plural descriptive name for the post type marked for translation.</li>
	<li>Singular Label (string) - A singular name for the post type.</li>
	<li>Description (string) - A short descriptive summary of what the post type is.</li>
	<li>Title (true/false) - Show the title field.</li>
	<li>Thumbnail (true/false) - Allow a featured image to be added.</li>
	<li>Editor (true/false) - Show the content (editor) field.</li>
	<li>Revisions (true/false) - Allow revisions.</li>
	<li>Hierarchical (true/false) - Whether the post type is hierarchical (e.g. page). Allows Parent to be specified.</li>
</ul>

